window.getLang = function () {
    try {
        return localStorage.getItem('selected_lang') || 'en';
    } catch (e) {
        return 'en';
    }
};

window.t = function (key) {
    const lang = window.getLang();
    if (translations[key] && translations[key][lang]) {
        return translations[key][lang];
    }
    if (translations[key] && translations[key].en) {
        return translations[key].en;
    }
    return key;
};

// Wraps localStorage in a try/catch to prevent the browser from halting JS execution
window.setLanguage = function (lang) {
    try {
        localStorage.setItem('selected_lang', lang);
    } catch (e) {
        console.warn("LocalStorage is blocked, language switch will only persist for this session:", e);
    }
    window.applyTranslations(lang);
};

window.applyTranslations = function (lang) {
    const activeLang = lang || window.getLang();

    document.querySelectorAll('[data-i18n]').forEach(function (element) {
        const key = element.getAttribute('data-i18n');
        element.innerHTML = window.t(key);
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(function (element) {
        const key = element.getAttribute('data-i18n-placeholder');
        element.placeholder = window.t(key);
    });

    document.querySelectorAll('[data-i18n-title]').forEach(function (element) {
        const key = element.getAttribute('data-i18n-title');
        const text = window.t(key);
        if (element.tagName === 'TITLE') {
            element.textContent = text;
        } else {
            element.setAttribute('title', text);
        }
    });

    // Updated to check against data-lang instead of onclick
    document.querySelectorAll('.lang-pill-btn').forEach(function (btn) {
        const btnLang = btn.getAttribute('data-lang');
        if (btnLang === activeLang) {
            btn.classList.add('active');
        } else {
            btn.classList.remove('active');
        }
    });

    document.documentElement.lang = activeLang;
    document.body.style.lineHeight = (activeLang === 'my') ? '1.6' : '';

    document.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang: activeLang } }));
};

document.addEventListener('DOMContentLoaded', function () {
    window.applyTranslations(window.getLang());
});